/**
 * 날짜를 기반으로 오늘의 메타인지적 질문 2개를 가져옵니다.
 * 같은 날짜에는 항상 같은 질문을 반환합니다.
 */
export function getTodaysQuestions(dateStr: string): string[] {
  // 날짜를 숫자로 변환하여 시드로 사용
  const [year, month, day] = dateStr.split("-").map(Number);
  const dayOfYear = getDayOfYear(year, month, day);

  // 두 개의 인덱스 계산 (서로 다른 질문)
  const index1 = dayOfYear % QUESTION_POOL.length;
  const index2 = (dayOfYear * 7 + 3) % QUESTION_POOL.length;

  return [QUESTION_POOL[index1], QUESTION_POOL[index2]];
}

/**
 * 연중 일자 계산 (1월 1일 = 1)
 */
function getDayOfYear(year: number, month: number, day: number): number {
  const daysInMonths = [0, 31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334];
  let dayOfYear = daysInMonths[month - 1] + day;

  // 윤년 처리
  if (month > 2 && isLeapYear(year)) {
    dayOfYear++;
  }

  return dayOfYear;
}

function isLeapYear(year: number): boolean {
  return (year % 4 === 0 && year % 100 !== 0) || year % 400 === 0;
}
